#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int	num;
	char			ch;

	if (n < 0)
	{
		write(1, "-", 1);
		num = n * -1;
	}
	else
		num = n;
	if (num >= 10)
		ft_putnbr(num / 10);
	ch = (num % 10) + 48;
	write(1, &ch, 1);
}
